#operators used in list
#concatination

l1=[1,2,3,4]
l2=[5,6,7,8]
print(l1+l2)

#reputations
print(l1*3)

#IN operators

print(2 in l1)
print(10 in l2)

#NOT-IN operators

print(3 not in l1)
print(10 not in l2)


#iteration through the list

list1=[10,20,30,40]
for i in list1:
    print(i)

#reverse
print(list1[::-1])